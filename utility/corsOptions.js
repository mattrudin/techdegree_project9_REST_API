/************************************************************************************
Allowed connections
************************************************************************************/
const whitelist = ['http://localhost']


/************************************************************************************
CORS options
************************************************************************************/
const corsOptions = {
    origin: function (origin, callback) {
        if (whitelist.indexOf(origin) !== -1 || !origin) {
            callback(null, true)
        } else {
            callback(new Error('Not allowed by CORS'))
        }
    }
}


/************************************************************************************
Export options
************************************************************************************/
module.exports = corsOptions;